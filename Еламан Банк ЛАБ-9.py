    @balance.setter
    def balance(self, amount):
        """–ë–∞–ª–∞–Ω—Å—Ç—ã “õ–∞—É—ñ–ø—Å—ñ–∑ —Ç“Ø—Ä–¥–µ –æ—Ä–Ω–∞—Ç—É (–∂–∞–∑—É) “Ø—à—ñ–Ω setter."""
